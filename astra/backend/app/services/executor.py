    def execute_plan(
        self,
        plan: Iterable[Any],
        project_name: str | None = None,
        request_text: str | None = None,
    ) -> dict:
        timestamp = datetime.utcnow().strftime("%Y%m%d_%H%M%S")
        safe_project_name = (project_name or "astra_project").strip().replace(" ", "_")
        output_dir = settings.generated_dir / f"{safe_project_name}_{timestamp}"
        output_dir.mkdir(parents=True, exist_ok=True)

        normalized_steps = list(plan)
        execution_log: list[str] = []
        for index, step in enumerate(normalized_steps, start=1):
            execution_log.append(f"Step {index}: {self._format_step(step)}")

        artifacts: list[str] = []
        lowered_request = (request_text or "").lower()
        if "landing page" in lowered_request or "landing" in lowered_request:
            artifacts.extend(self._create_landing_page_project(output_dir, safe_project_name, request_text or ""))
            execution_log.append("Generated a complete landing page implementation")
        else:
            artifacts.extend(self._create_starter_project(output_dir, safe_project_name))

        readme_path = output_dir / "README.md"
        readme_path.write_text(
            "# ASTRA Generated Project\n\n"
            + "\n".join(f"- {self._format_step(step)}" for step in normalized_steps)
            + "\n\n## Created Files\n"
            + "\n".join(f"- {Path(path).name}" for path in artifacts)
            + "\n",
            encoding="utf-8",
        )
        artifacts.append(str(readme_path))
        execution_log.append(f"Created {len(artifacts)} project files in {output_dir}")

        return {
            "project_path": str(output_dir),
            "artifacts": artifacts,
            "execution_log": execution_log,
        }


    def write_code_file(self, filename: str, code: str) -> str:
        settings.generated_dir.mkdir(parents=True, exist_ok=True)
        safe_filename = filename.replace("..", "").replace("\\", "/").split("/")[-1]
        target_path = settings.generated_dir / safe_filename
        target_path.write_text(code, encoding="utf-8")
        return str(target_path)

    @staticmethod
    def _format_step(step: Any) -> str:
        if isinstance(step, dict):
            step_number = step.get("step")
            action = step.get("action", "step")
            value = step.get("value", "")
            if value:
                prefix = f"{step_number}. " if step_number is not None else ""
                return f"{prefix}{action}: {value}"
            return f"{step_number}. {action}" if step_number is not None else str(action)

        return str(step)


